async function seed() {
    console.log("ðŸŒ± Seeding DFY Products...");

    for (const product of FM_PRODUCTS) {
        const existing = await prisma.dFYProduct.findUnique({
            where: { slug: product.slug },
        });

        if (existing) {
            console.log(`  â­ï¸  Skipping ${product.title} (already exists)`);
            continue;
        }

        await prisma.dFYProduct.create({
            data: {
                ...product,
                price: product.price,
                compareAtPrice: product.compareAtPrice,
            },
        });
        console.log(`  âœ… Created: ${product.title}`);
    }

    console.log("\nðŸŽ‰ Seeding complete!");
    console.log(`   Created ${FM_PRODUCTS.length} Functional Medicine products`);
}

seed()
    .catch(console.error)
    .finally(() => prisma.$disconnect());
